<script>
document.querySelectorAll('.bike-trigger').forEach(bike => {
  bike.addEventListener('click', function () {

    const key = this.dataset.bike;
    const data = bikeFeatures[key];
    if (!data) return;

    // Show section
    const section = document.getElementById('services');
    section.classList.remove('d-none');
    section.scrollIntoView({ behavior: 'smooth' });

    // Load features
    fillList('battery-list', data.battery);
    fillList('braking-list', data.braking);
    fillList('comfort-list', data.comfort);
    fillList('utility-list', data.utility);
    fillList('safety-list', data.safety);

    // Reset tabs
    document.querySelectorAll('.nav-link').forEach(t => t.classList.remove('active'));
    document.querySelectorAll('.tab-pane').forEach(p => p.classList.remove('show','active'));

    // Activate first tab (Battery)
    document.querySelector('[data-bs-target="#tab-pane-1"]').classList.add('active');
    document.getElementById('tab-pane-1').classList.add('show','active');
  });
});

function fillList(id, items) {
  const ul = document.getElementById(id);
  if (!ul) return;
  ul.innerHTML = "";
  items.forEach(item => {
    ul.innerHTML += `<li><i class="fa fa-check text-success me-2"></i>${item}</li>`;
  });
}
</script>


